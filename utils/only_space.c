#include "../cub.h"

int	only_spaces(char *str)
{
	int	i;

	i = -1;
	while (str[++i])
	{
		if (str[i] != ' ' || str[i] != '1' || str[i] != '2')
			return (0);
	}
	if (str[0] == '\0')
		return (0);
	return (1);
}
